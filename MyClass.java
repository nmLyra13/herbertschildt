package herbertschildt;

public class MyClass {
	public int beta;
	public int gama;
	private int alpha;
	
	public MyClass() {		
	}
	
	public MyClass(int beta, int gama, int alpha) {
		super();
		this.beta = beta;
		this.gama = gama;
		this.alpha = alpha;
	}

	public int getBeta() {
		return beta;
	}

	public void setBeta(int beta) {
		this.beta = beta;
	}

	public int getGama() {
		return gama;
	}

	public void setGama(int gama) {
		this.gama = gama;
	}

	public int getAlpha() {
		return alpha;
	}

	public void setAlpha(int alpha) {
		this.alpha = alpha;
	}	
	
	
	
	
}
